# 什么是记忆模板？
# --------------
# 记忆模板是 Agent 长期记忆文件的初始结构。
# 当用户首次使用 nanobot 时，会创建 memory/MEMORY.md 文件，
# 用于存储 Agent 的长期记忆。
#
# 记忆文件结构：
# ------------
# memory/
# ├── MEMORY.md    # 长期记忆（持久化事实）
# └── HISTORY.md   # 历史日志（对话摘要）
#
# MEMORY.md 内容示例：
# -----------------
