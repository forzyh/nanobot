# 这是 nanobot 模板模块的入口文件。
#
# 什么是模板？
# ----------
# 模板是预定义的文件或目录结构，用于快速初始化项目。
# nanobot 使用模板来：
# 1. 创建工作空间：初始化 Agent 工作目录
# 2. 复制引导文件：AGENTS.md、SOUL.md 等
# 3. 提供示例配置：帮助用户快速上手
#
# 模板目录结构：
# ------------
# templates/
# ├── memory/          # 记忆目录模板
# │   └── MEMORY.md    # 长期记忆文件模板
# └── workspace/       # 工作空间模板
#     ├── AGENTS.md    # Agent 行为规范
#     ├── SOUL.md      # Agent 个性设定
#     └── skills/      # 技能目录
#
# 使用场景：
# --------
# 当用户运行 `nanobot onboard` 时：
# 1. 创建 ~/.nanobot/workspace 目录
# 2. 从 templates 复制模板文件
# 3. 用户可以编辑这些文件自定义 Agent 行为
